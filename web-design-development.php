									<h3>OUTCOME</h3>
									<p>
									To bring all the parts of Pocket together, I have developed a visual language based on the existing branding for Co-operative Bank. We chose to bring in the secondary colours to add vibrance to Pocket without making it too childlike. The use of circles allowed us to make written information more visually interesting. The use of icons is similar to those found in games and social media - they are simple yet still descriptive. The tone of voice used was really important as it personalises the experience: making Zoe feel like Pocket is designed especially for her.
									<br>
