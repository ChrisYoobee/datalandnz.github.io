									<h3>THE CHALLENGE</h3>
									<p>
										The Co-operative Bank have identified a gap within the banking market; teenagers and their first interactions with a bank. Gaining customers at this point within their banking lifecycle is incredibly important as many customers will stay with their first bank until have a substantial reason to change. The Co-operative Bank want to revolutionise and re-think the banking sector and push to market a mobile app which allows teenagers to do their basic banking needs via a youthful and trendy mobile app that has a unique experience. The aim of this app is to start teaching the basics of financial literacy and shaping future spending and saving habits. We had one day from 9am until 5pm to come up with a solution.
									</p>
								</div>
								<div class="text-spacing">
									<h3>RESEARCH & INSPIRATION</h3>
									<p>
										When thinking about designing a teenage banking service I needed to really understand what makes them tick. I had a chat with our friend Zoe. Zoe’s about to turn 13. The only time she deals with money is spending her pocket money on dance accessories. She doesn’t find banking interesting but she is excited about getting her first eftpos card. Zoe’s mum is not as excited about this part. However, she does want Zoe to have independence and learn the value of money.
										<br>
										<br>
										Based on our research of teens and our chat with Zoe we were able to define the key needs for those using our banking service. They need to gain some independence by using the app. They need to feel rewarded in order to enjoy the experience. They have little knowledge of banking so need to be guided through their experience. And lastly, they need to develop positive habits that are encouraged by the app.
										<br>
										<br>
										With these needs in mind, I started to seek out existing digital platforms that were working towards similar goals. ASB’s Clever Kash works well as it encourages children to value money by reintroducing physical coins into the equation. Pocopay rewards the user through animate bubble gestures that give a response to user interactions. Mint guides the user by colour coding and simplifying information. We also were inspired by the visual language used in other digital banking services particularly the use of iconography, circles, vibrant colours and data visualisation.
									</p>
