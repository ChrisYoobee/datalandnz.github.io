									<h3>THE MOCKUPS</h3>
									<p>

										There’s many things that makes Pocket great but the four key functions we’re going to mention are the dashboard, goals, awards and transactions. I'll begin just like the user does, on the dashboard. Zoe only has two accounts so we’ve developed an easier way for her to navigate through them by swiping right. Her navigation bar is built in a similar style to that of her phone. We purposefully avoided large drop down menus and stripped back the functions to exactly what Zoe would need. This menu could be customisable so that as Zoe grows and her banking needs change, she can drag and drop in different functions.
										<br>
										<br>
										When Zoe views her transactions through Pocket, she sees her spendings and earnings like a journey. Each day is represented like a path so that she sees the relationship between her transactions rather than one endless list. The goals section of Pocket brings the interactive style of Pocopay to life. Zoe can drag the outer circle to set her goal. As she saves, the inner circle grows until finally Yay! She reaches her goal and Pocket celebrates with some confetti! The animated nature of this function shows Zoe a visual representation of money. It creates a lifelike quality to the app where the it shrinks and grows depending on her actions. The awards part of Pocket encourages good banking habits. Zoe receives different awards based on positive interactions she’s had i.e Saving for three weeks in a row or setting up your first savings account. This awards system provides Zoe with an incentive to keep using Pocket. In order to bring the social aspect of popular apps into Pocket, I also proposed that these awards could be viewed and shared by Zoe’s friends. This is a better way of comparing achievements without actually sharing private information.

									</p>

