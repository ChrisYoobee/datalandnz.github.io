								<h3>THE CHALLENGE</h3>
								<p>
									This was a two day hack-fest, hosted at Powershop. In teams of 5 split up of 1 designer and 4 coders we had to make a web application with api data relevant to New Zealand. The idea I came up with was to use the Trade Me api to find out average rent prices in different areas to make deciding which area to rent a less painful task. The main user in mind was people moving to a new city and looking for the cheapest place to rent, relative to the city. I pushed this idea further by adding in apis relevant to crime rate in areas as well as heating/electrical costs.
								</p>
								<h3>REASEARCH</h3>
								<p>
									​I first researched what services like mine were out there. I found that there were a range of services that looked at average prices like numbeo.com for example. You input two cities around the world and it gave you price comparisons. I found this was a common service provided by many sites and where flat smart differed itself by just focusing in on New Zealand and going one step further to compare suburbs.
								</p>
								<h3>WIREFRAMES</h3>
								<p>
									I wanted this web application to be simplistic for the user so I ended up going for 2 inputs on the home page and then have all the information appear below after the search button was pressed. A simple user navigation below shows how the user can make multiple searches with ease, since there is purposely as little steps as possible. I also explored and did iterations with data visualisation. This is an important part since information needs to be conveyed to the user easily so they can digest all the information quickly and clearly. With final wireframes I chose to go with stats paired with a shape so when they make multiple searches they will instantly identify the shapes with a certain bit of information, making it faster to go through the information.
